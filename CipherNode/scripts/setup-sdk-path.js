const fs = require('fs');
const path = require('path');
const os = require('os');

const home = os.homedir();
const platform = os.platform();

let sdkDir = '';

// Check environment variables first
if (process.env.ANDROID_HOME && fs.existsSync(process.env.ANDROID_HOME)) {
    sdkDir = process.env.ANDROID_HOME;
} else if (process.env.ANDROID_SDK_ROOT && fs.existsSync(process.env.ANDROID_SDK_ROOT)) {
    sdkDir = process.env.ANDROID_SDK_ROOT;
} else {
    // Check standard locations
    const candidates = [];
    if (platform === 'darwin') {
        candidates.push(
            path.join(home, 'Library/Android/sdk'),
            '/opt/homebrew/share/android-sdk',
            '/usr/local/share/android-sdk'
        );
    } else if (platform === 'win32') {
        candidates.push(
            path.join(process.env.LOCALAPPDATA || path.join(home, 'AppData/Local'), 'Android/Sdk')
        );
    } else {
        candidates.push(
            path.join(home, 'Android/Sdk'),
            '/usr/lib/android-sdk'
        );
    }

    for (const cand of candidates) {
        if (fs.existsSync(cand)) {
            sdkDir = cand;
            break;
        }
    }
}

const androidDir = path.join(__dirname, '../android');
const propertiesPath = path.join(androidDir, 'local.properties');

if (!fs.existsSync(androidDir)) {
    console.error('Error: android directory not found. Are you running this script from the root of the project?');
    process.exit(1);
}

// Ensure local.properties exists or write to it
let sdkPathForFile = sdkDir || path.join(home, 'Library/Android/sdk');

// Escape backslashes for Windows paths in properties files
if (platform === 'win32') {
    sdkPathForFile = sdkPathForFile.replace(/\\/g, '\\\\');
}

const content = `## This file is automatically generated by setup-sdk-path.js\n# Location of the SDK. This is only used by Gradle.\nsdk.dir=${sdkPathForFile}\n`;

fs.writeFileSync(propertiesPath, content, 'utf8');

if (sdkDir) {
    console.log(`\x1b[32mSuccess:\x1b[0m Configured local.properties with detected Android SDK path: ${sdkDir}`);
} else {
    console.log(`\x1b[33mWarning:\x1b[0m Android SDK could not be auto-detected in standard locations.`);
    console.log(`Configured fallback path in local.properties: ${sdkPathForFile}`);
    console.log(`\nPlease install Android Studio or the Android SDK command-line tools to proceed with native compilation.`);
}
